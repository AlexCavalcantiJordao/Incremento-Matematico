package incrememnto.matematico;

public class IncrememntoMatematico {

    /* Método Principal da classe */
    public static void main(String[] args) {
        int x = 8;
        int y = ++x; // Pré-Requisito é quando o "++x" fica na frente do valor dar um item a mais no valor. Agora Pós-Requisito é quando o "x++" deixa o mesmo valor que é incrementado.
        System.out.println("X: " + x);
        System.out.println("Y: " + y);
    }// Fim do método...
}// Fim da classe...
